export function buildSchemas(page) {
  const breadcrumbs = {
    "@context": "https://schema.org",
    "@type": "BreadcrumbList",
    itemListElement: [
      {
        "@type": "ListItem",
        position: 1,
        name: "მთავარი",
        item: siteUrl,
      },
      {
        "@type": "ListItem",
        position: 2,
        name: page.h1,
        item: page.url,
      },
    ],
  };

  const service = {
    "@context": "https://schema.org",
    "@type": "Service",
    name: page.h1,
    serviceType: page.serviceType,
    url: page.url,
    description: page.description,
    provider: {
      "@type": "ProfessionalService",
      name: "Next-Hub Solutions",
      url: siteUrl,
      telephone: "+995555137003",
      areaServed: "საქართველო",
      logo: `${siteUrl}/logo.svg`,
    },
    areaServed: {
      "@type": "Country",
      name: "საქართველო",
    },
    offers: page.pricing.map((plan) => ({
      "@type": "Offer",
      name: plan.name,
      priceCurrency: "GEL",
      price: plan.price.replace(/[^0-9]/g, "") || "0",
      availability: "https://schema.org/InStock",
    })),
  };

  const faq = {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: page.faqs.map((item) => ({
      "@type": "Question",
      name: item.question,
      acceptedAnswer: {
        "@type": "Answer",
        text: item.answer,
      },
    })),
  };

  return [breadcrumbs, service, faq];
}

